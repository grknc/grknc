### CUSTOMER LIFETIME VALUE ####

# Bir müşterinin bir şirketle kurduğu ilişki-iletşim süresince bu şirkete kazandıracağı parasal değer.

# Customer Lifetime Value

# Müşteri YaşamBoyu Değeri

# Yaşam Boyu Değeri

# satınalma başına ortalama kazanç * satın alma sayısı

# CLTV = (Customer Value / Churn Rate)* Profit Margin

# Customer Value = Average Order Value * Purchase Frequency ( Main Focus)

# Churn Rate : Müşteri Terk Oranı

# Profit Margin

# Average Order Value = Total Price / Total Transaction

# Purchase Frequency = Total Transaction / Total Number of Customers

# Toplam İşlem Sayısı / Total Müşteri Sayısı  = Satın alma Frekansı ( Ölçeklendirme)

# Churn Rate = 1- Repeat Rate ( Müşteri Terk Oranı)

# Repeat Rate = Birden fazla alışveriş yapan müşteri sayısı / tüm müşteriler

# Profit Margin = Total Price * 0.10

# Total Number of Customers : 100
# Churn Rate : 0.8
# Profit : 0.10

# CLTV değerlerine göre bir sıralama yapılıp belli noktalarda gruplama yapılarak segmentlere ayrılmış olacak.




